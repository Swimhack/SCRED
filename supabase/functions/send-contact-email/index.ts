      if (!gmailUser || !gmailPassword) {
        console.error('Gmail credentials not configured')
        // Update submission to mark email error but don't fail the request
        await supabase
          .from('contact_submissions')
          .update({ 
            email_error: 'Gmail credentials not configured',
            email_sent: false
          })
          .eq('id', submission.id)

        return new Response(
          JSON.stringify({ 
            success: true, 
            message: 'Contact form submitted successfully', 
            id: submission.id,
            emailWarning: 'Email notification could not be sent'
          }),
          { 
            status: 200, 
            headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
          }
        )
      }

      // Prepare email content
      const emailSubject = `ğŸ”” New Contact Form Submission from ${name}`
